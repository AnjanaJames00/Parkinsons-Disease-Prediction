import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.model_selection import RandomizedSearchCV
from sklearn.metrics import accuracy_score, classification_report, roc_curve, auc
from lightgbm import LGBMClassifier, plot_importance

# Clean column names for LightGBM compatibility
X_train.columns = X_train.columns.str.replace('[^A-Za-z0-9_]+', '_', regex=True)
X_val.columns = X_val.columns.str.replace('[^A-Za-z0-9_]+', '_', regex=True)

# Define base model
lgb = LGBMClassifier(random_state=42)

# Parameter grid for tuning (smaller, efficient)
param_dist = {
    'num_leaves': [15, 31, 63],
    'max_depth': [3, 5, 7, -1],
    'learning_rate': [0.01, 0.05, 0.1],
    'n_estimators': [100, 200, 300],
    'min_child_samples': [10, 20, 30],
    'subsample': [0.7, 0.8, 1.0],
    'colsample_bytree': [0.7, 0.8, 1.0],
    'reg_alpha': [0, 0.1, 0.3],
    'reg_lambda': [0, 0.1, 0.3]
}

# Randomized Search (tries 30 random combinations)
random_search = RandomizedSearchCV(
    estimator=lgb,
    param_distributions=param_dist,
    n_iter=30,            # number of random combinations
    scoring='accuracy',
    cv=5,
    random_state=42,
    n_jobs=-1,
    verbose=1
)

# Fit model on training data
random_search.fit(X_train, Y_train)

# Best model
best_lgbm = random_search.best_estimator_

print("\n✅ Best Parameters for LightGBM:", random_search.best_params_)
print("📊 Best Cross-Validation Accuracy:", random_search.best_score_)

# Evaluate on validation set
y_pred_lgb = best_lgbm.predict(X_val)
val_accuracy = accuracy_score(Y_val, y_pred_lgb)

print("\n🎯 Validation Accuracy:", val_accuracy)
print("\nClassification Report:\n", classification_report(Y_val, y_pred_lgb))

# ============================================
# 📈 ROC Curve
# ============================================
y_pred_proba = best_lgbm.predict_proba(X_val)[:, 1]
fpr, tpr, _ = roc_curve(Y_val, y_pred_proba)
roc_auc = auc(fpr, tpr)

plt.figure(figsize=(6, 5))
plt.plot(fpr, tpr, color='darkorange', lw=2, label=f'ROC Curve (AUC = {roc_auc:.2f})')
plt.plot([0, 1], [0, 1], color='navy', lw=2, linestyle='--')
plt.xlabel('False Positive Rate')
plt.ylabel('True Positive Rate')
plt.title('LightGBM ROC Curve')
plt.legend(loc='lower right')
plt.show()

