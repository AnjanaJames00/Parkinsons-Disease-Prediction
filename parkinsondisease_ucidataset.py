import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd
from sklearn.metrics import (
    accuracy_score, precision_score, recall_score,
    f1_score, roc_curve, auc, confusion_matrix, classification_report
)
from sklearn.linear_model import LogisticRegression
from sklearn.tree import DecisionTreeClassifier
from sklearn.ensemble import RandomForestClassifier
from xgboost import XGBClassifier
from lightgbm import LGBMClassifier

# ===============================================
# 🧠 Initialize Models
# ===============================================

models = {
    "Logistic Regression": LogisticRegression(max_iter=1000, random_state=42),
    "Decision Tree": DecisionTreeClassifier(random_state=42),
    "Random Forest": RandomForestClassifier(n_estimators=200, max_depth=8, random_state=42),
    "XGBoost": XGBClassifier(learning_rate=0.1, max_depth=5, n_estimators=200,
                             use_label_encoder=False, eval_metric='logloss', random_state=42),
    "LightGBM": LGBMClassifier(num_leaves=31, max_depth=5, learning_rate=0.05, n_estimators=300,
                               reg_alpha=0.1, reg_lambda=0.1, subsample=0.8, colsample_bytree=0.8,
                               random_state=42)
}

# ===============================================
# 📈 Train and Evaluate Models
# ===============================================

results = []
plt.figure(figsize=(8, 6))

for name, model in models.items():
    model.fit(X_train, Y_train)
    y_pred = model.predict(X_val)
    y_prob = model.predict_proba(X_val)[:, 1]

    acc = accuracy_score(Y_val, y_pred)
    prec = precision_score(Y_val, y_pred)
    rec = recall_score(Y_val, y_pred)
    f1 = f1_score(Y_val, y_pred)

    results.append({
        'Model': name,
        'Accuracy (%)': round(acc * 100, 2),
        'Precision (%)': round(prec * 100, 2),
        'Recall (%)': round(rec * 100, 2),
        'F1-Score (%)': round(f1 * 100, 2)
    })

    # ROC Curve
    fpr, tpr, _ = roc_curve(Y_val, y_prob)
    roc_auc = auc(fpr, tpr)
    plt.plot(fpr, tpr, label=f'{name} (AUC = {roc_auc:.2f})')

# ===============================================
# 🎯 Plot ROC Curve Comparison
# ===============================================

plt.plot([0, 1], [0, 1], 'k--')
plt.xlabel('False Positive Rate')
plt.ylabel('True Positive Rate')
plt.title('ROC Curve Comparison Across Models')
plt.legend()
plt.show()

# ===============================================
# 📊 Show Performance Table
# ===============================================

results_df = pd.DataFrame(results).sort_values(by='Accuracy (%)', ascending=False)
display(results_df)

# ===============================================
# 📉 Confusion Matrix for Best Model
# ===============================================

best_model_name = results_df.iloc[0]['Model']
best_model = models[best_model_name]
y_pred_best = best_model.predict(X_val)

plt.figure(figsize=(6, 4))
sns.heatmap(confusion_matrix(Y_val, y_pred_best), annot=True, fmt='d', cmap='Greens')
plt.title(f'{best_model_name} Confusion Matrix')
plt.xlabel('Predicted')
plt.ylabel('Actual')
plt.show()

print(f"✅ Best Performing Model: {best_model_name}")
print("\nClassification Report:\n")
print(classification_report(Y_val, y_pred_best))

